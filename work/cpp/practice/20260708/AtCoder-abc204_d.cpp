#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;
    vector<int> t(n);
    int sum = 0;
    for (int i = 0; i < n; i++) {
        cin >> t[i];
        sum += t[i];
    }

    // 定义dp[t]为是否能凑出时间t，规则：若t能凑出来，则t + t[i]也能
    vector<int> dp(sum + 1, 0);
    dp[0] = 1;

    /*
    具体例子：
    n = 5
    t = 8 3 7 2 5

    sum = 25

    两个烤箱同时工作，所以我们要把这些菜分成两组：
    烤箱 A 的总时间是 j
    烤箱 B 的总时间是 sum - j

    最终完成时间是：
    max(j, sum - j)

    所以问题变成：
    能不能选出一些菜，让它们的总时间 j 尽量接近 sum / 2。

    dp[j] 的意思：
    当前已经看过一些菜，能不能从里面选出若干道，使总时间刚好是 j。

    一开始什么都不选：
    dp[0] = 1

    处理第 1 道菜，时间是 8：
    原来能凑出：0
    加上 8 后又能凑出：8
    现在能凑出：0, 8

    处理第 2 道菜，时间是 3：
    原来能凑出：0, 8
    加上 3 后又能凑出：
    0 + 3 = 3
    8 + 3 = 11
    现在能凑出：0, 3, 8, 11

    处理第 3 道菜，时间是 7：
    原来能凑出：0, 3, 8, 11
    加上 7 后又能凑出：
    0 + 7 = 7
    3 + 7 = 10
    8 + 7 = 15
    11 + 7 = 18
    现在能凑出：0, 3, 7, 8, 10, 11, 15, 18

    处理第 4 道菜，时间是 2：
    继续在原来能凑出的时间上加 2。
    比如：
    0 + 2 = 2
    3 + 2 = 5
    8 + 2 = 10
    11 + 2 = 13

    处理第 5 道菜，时间是 5：
    继续在原来能凑出的时间上加 5。

    最后可以凑出：
    12 = 7 + 3 + 2
    13 = 8 + 5

    也就是说可以分成：
    烤箱 A：7, 3, 2，总时间 12
    烤箱 B：8, 5，总时间 13

    两个烤箱同时开始，最终完成时间就是较大的那边：
    max(12, 13) = 13

    下面这段循环的本质：
    每来一道新菜 t[i]，就把所有“原来能凑出的时间”都加上 t[i]，
    得到新的可凑时间。

    if (dp[j - t[i]]) dp[j] = 1;
    翻译成人话就是：
    如果以前能凑出 j - t[i]，
    那么现在加上当前这道菜 t[i]，
    就能凑出 j。

    j 必须倒着枚举：
    因为每道菜只能用一次。
    如果正着枚举，可能会在同一轮里重复使用当前这道菜。
    */

    for (int i = 0; i < n; i++) {
        for (int j = sum; j >= t[i]; j--) {
            if (dp[j - t[i]]) {
                dp[j] = 1;
            }
        }
    }

    int ans = sum;
    for (int j = 0; j <= sum; j++) {
        if (dp[j]) {
            ans = min(ans, max(j, sum - j));
        }
    }

    cout << ans << endl;

    return 0;
}
