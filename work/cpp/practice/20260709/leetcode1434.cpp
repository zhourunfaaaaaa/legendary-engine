#include <bits/stdc++.h>
using namespace std;

class Solution {
public:
    int numberWays(vector<vector<int>>& hats) {
        const int MOD = 1000000007;

        int n = hats.size();

        vector<vector<int>> people(41);

        // 转换为每一顶帽子可以被哪些人选
        for (int i = 0; i < n; i++) {
            for (int h : hats[i]) {
                people[h].push_back(i);
            }
        }

        int full = (1 << n) - 1;

        vector<int> dp(1 << n, 0);
        dp[0] = 1;

        /*
        具体例子：hats = [[3,4], [4,5], [5]]

        一共有 3 个人：
        第 0 个人喜欢 3, 4
        第 1 个人喜欢 4, 5
        第 2 个人喜欢 5

        先反过来整理成 people：
        people[3] = {0}
        people[4] = {0, 1}
        people[5] = {1, 2}

        mask 表示哪些人已经有帽子：
        000：没人有帽子
        001：第 0 个人有帽子
        010：第 1 个人有帽子
        011：第 0、1 个人有帽子
        100：第 2 个人有帽子
        111：三个人都有帽子

        一开始：
        dp[000] = 1
        表示还没处理任何帽子，没人有帽子，这种情况有 1 种。

        处理帽子 3：
        帽子 3 只能给第 0 个人。

        从 dp[000] = 1 出发：
        1. 不用帽子 3，状态还是 000，所以 ndp[000] += 1
        2. 给第 0 个人，状态变成 001，所以 ndp[001] += 1

        处理完帽子 3：
        dp[000] = 1
        dp[001] = 1

        处理帽子 4：
        帽子 4 可以给第 0、1 个人。
        先 vector<int> ndp = dp，表示“不用帽子 4”的情况先保留下来：
        ndp[000] = 1
        ndp[001] = 1

        从 dp[000] = 1 出发：
        给第 0 个人：000 -> 001，所以 ndp[001] += 1
        给第 1 个人：000 -> 010，所以 ndp[010] += 1

        从 dp[001] = 1 出发：
        第 0 个人已经有帽子了，不能再给第 0 个人。
        给第 1 个人：001 -> 011，所以 ndp[011] += 1

        处理完帽子 4：
        dp[000] = 1
        dp[001] = 2
        dp[010] = 1
        dp[011] = 1

        dp[001] = 2 的意思是：
        让第 0 个人有帽子的方案有 2 种：
        1. 第 0 个人戴帽子 3
        2. 第 0 个人戴帽子 4

        处理帽子 5：
        帽子 5 可以给第 1、2 个人。

        从 dp[001] = 2 出发：
        给第 1 个人：001 -> 011，所以 ndp[011] += 2
        给第 2 个人：001 -> 101，所以 ndp[101] += 2

        从 dp[011] = 1 出发：
        第 1 个人已经有帽子了，不能再给第 1 个人。
        给第 2 个人：011 -> 111，所以 ndp[111] += 1

        最后 dp[111] = 1。
        111 表示三个人都有帽子，所以答案是 1。

        这 1 种方案就是：
        第 0 个人戴 3，第 1 个人戴 4，第 2 个人戴 5。

        这段循环的本质：
        dp  是处理当前帽子之前的状态表。
        ndp 是处理当前帽子之后的状态表。
        当前帽子要么不用，要么给一个喜欢它且还没帽子的人。
        */

        // 对每一顶帽子
        for (int h = 1; h <= 40; h++) {
            // 若该顶帽子没人要选则保持原来的方法
            vector<int> ndp = dp;

            for (int mask = 0; mask <= full; mask++) {
                if (dp[mask] == 0) continue;

                for (int p : people[h]) {
                    // 判断第p人是否有帽子
                    if ((mask & (1 << p)) == 0) {
                        // newMask就是mask加上第p人
                        int newMask = mask | (1 << p);
                        ndp[newMask] = (ndp[newMask] + dp[mask]) % MOD;
                    }
                }
            }

            // 把新表复制到旧表上
            dp = ndp;
        }

        return dp[full];
    }
};
